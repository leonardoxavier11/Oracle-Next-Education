package com.loiane.cursojava.aula13;

public class OperadoresAritmeticos {

	public static void main(String[] args) {

		int resultado = 4;
		
		System.out.println(resultado++); //imprime resultado e dps soma
		System.out.println(resultado); // imprime resultado atualizado
		
		int resultado2 = 4;
		System.out.println(++resultado2); //soma e imprima o resultado
	}

}
